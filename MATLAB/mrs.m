% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

dp=str2double(get(handles.edit7,'string'));
ds=str2double(get(handles.edit6,'string'));
f1=str2double(get(handles.edit1,'string'));
f2=str2double(get(handles.edit2,'string'));
fp=str2double(get(handles.edit3,'string'));
com=(sscanf(get(handles.edit5,'string'),'%d')).'

if get(handles.radiobutton1,'value')==1
    dp=10^(dp/20)-1,
    ds=10^(-ds/20),
end


st=length(com);
dp=dp/st
if f1>f2
    M=f1/f2
else
    L=f2/f1
end
ft=f1
for j=1:st
pat(j)=ft/com(j)
ft=pat(j)
end
pat=[f1 pat]

for i=1:st
    
    Fsbe(i)=pat(i+1)-(f1/(M*2))
    twn(i)=(Fsbe(i)-fp)/pat(i)
    N(i)=points(dp,ds,twn(i))

end
    
    TSR=sum(N)
    i=1:st;
    MPS=sum(N(i).*pat(i+1))
g=sprintf('MPS: %05.2f K  &  TSR: %05.2f',MPS,TSR)


set(handles.text1,'string',g)
set(handles.text9,'string',N.')
set(handles.text10,'string',pat.')




